UPDATE employee
SET name = 'Ahmet'  --ID'si 2 olan kişinin name alanının güncellenmesi
WHERE id = 2;

UPDATE employee
SET email = 'example@hotmail.com'  --ID'si 5'ten küçük olanların email alanının güncellenmesi
WHERE id < 5;

UPDATE employee
SET birthday = '2001-01-01'       --İsmi S ile başlayanların doğum tarihinin güncellenmesi
WHERE name LIKE 'S%'
RETURNING *;

UPDATE employee
SET name = 'YoungEmployee'            -- Doğum günü 2017-01-01'den küçük olanların isminin güncellenmesi
WHERE birthday > '2017-01-01';          

